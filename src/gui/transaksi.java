    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(TFjumlahaset.getText().equals("") || TFharga.getText().equals("")){
            //Input Tidak Boleh Kosong
            JOptionPane.showMessageDialog(null, "Input Tidak Boleh Kosong");
        } else {
        int jumlahaset = Integer.valueOf(TFjumlahaset.getText());
        int harga = Integer.parseInt(TFharga.getText());
        
        int hasil = jumlahaset * harga;
     
        TFtotal.setText(Integer.toString(hasil));
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

