    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        Transaksi ck = new Transaksi();
        ck.id_transaksi = TFid.getText();
        ck.tanggal_transaksi = TFtanggal.getText();
        ck.jumlah_aset = TFjumlahaset.getText();      
        ck.harga_aset = TFharga.getText();
        ck.nama_aset = TFnama.getText();
        ck.total_jumlah = TFtotal.getText();
        try{
            ck.simpan();
            JOptionPane.showMessageDialog(null, "Simpan");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }ViewData();
    }//GEN-LAST:event_btnsimpanActionPerformed

